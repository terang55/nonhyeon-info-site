          list="apartment-options"
          placeholder="Îã®ÏßÄÎ™ÖÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî (Ïòà: ÏóêÏΩîÎ©îÌä∏Î°ú)"
